import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const root = process.cwd();
const sourcePath = resolve(root, "prisma", "schema.prisma");
const destinationPath = resolve(root, "prisma", "schema.dev.prisma");
const source = await readFile(sourcePath, "utf8");
const sqliteSchema = source
  .replace('output   = "../src/generated/prisma"', 'output   = "../src/generated/sqlite"')
  .replace('provider = "postgresql"', 'provider = "sqlite"')
  .replace(/\s+@db\.Uuid/g, "")
  // Prisma's SQLite DDL currently emits unquoted object/array JSON defaults.
  // Nullable JSON preserves the production shape while application writes stay explicit.
  .replace(/Json(\s+)@default\("(?:\{\}|\[\])"\)/g, "Json?$1");

await writeFile(
  destinationPath,
  `// Generated from schema.prisma. Do not edit this file directly.\n${sqliteSchema}`,
  "utf8"
);
